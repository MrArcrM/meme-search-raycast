/**
 * 表情包打标 prompt 集合。
 *
 * 抽离自 tag.ts 是为了：
 * 1) prompt 是核心资产，独立成模块便于阅读、迭代、对比；
 * 2) 改 prompt 不用动业务逻辑；
 * 3) 多 provider（Anthropic SDK / Claude CLI / Gemini CLI / 批量）共用同一套设计原则，集中维护。
 *
 * ─────────────────────────────────────────────────────────
 * 设计原则（2026-05-21 大版本升级后）
 * ─────────────────────────────────────────────────────────
 *
 * 1. **意图层 tag 硬要求 ≥6 个**
 *    搜表情包靠"嫌弃""傲娇""敷衍"这种意图词，不是"光头""嘟嘴"这种外观词。
 *    早期 prompt 没强制，模型偏好描述外观，导致用户搜"无奈"召回率低。
 *
 * 2. **显式忽略系列水印**
 *    很多表情包系列右上角带固定小水印（"Na" / "月巴" / "ANG" 等系列标识 / Logo / 账号 ID）。
 *    把它们当字幕 OCR 会污染搜索（搜"月巴"错命中、搜"Na" 噪音命中）。
 *    has_text 的判断依据是"有无字幕"，不是"有无水印"。
 *
 * 3. **角色名仅 100% 确定时才标**
 *    防止 LLM 幻觉角色名（Antigravity 实测会编造"谢飞机"等不存在的角色）。
 *
 * 4. **GIF 时序理解：16 帧 4×4 拼图**（详见 [tag.ts:prepareGridImage]）
 *    旧版 9 帧 3×3 对动作复杂的 gif（如"闭眼摇头表达无奈"）会判错（只看到前段"皱眉"判愤怒）。
 *    16 帧覆盖更完整时间线，让模型抓住"整段动作的意图"而非单帧表情。
 *
 * 5. **输出格式：Claude 走 XML，Anthropic SDK 走 JSON**
 *    Claude 在 JSON 字符串里嵌中文引号常忘转义导致 JSON.parse 失败，XML 没这个问题。
 *    SDK 路径已经稳了所以保留 JSON。
 *
 * 实验记录：~/.claude/projects/.../memory/experiment_meme_tagging_pipeline_2026-05-21.md
 */

/**
 * Anthropic SDK provider 用（输出 JSON）。
 *
 * 注：当前仅在用户显式提供 ANTHROPIC_API_KEY 时走此路径（按消费计费），
 * 默认走下面的 Claude CLI（XML 输出）复用 Max 配额。
 */
export const SYSTEM_PROMPT = `你是一个表情包打标助手。用户会给你一张表情包图片（如果原图是 GIF，已抽取最有代表性的一帧）。

严格按以下 JSON schema 输出（只输出 JSON 本体，不要 markdown 代码块、不要前言、不要解释）：

{
  "description": string,          // 一句话客观描述图片内容（人物、动作、场景），不超过 30 字
  "tags": string[],               // 10-20 个搜索关键词，中文，多角度覆盖；至少 6 个意图/情绪/语义类
  "emotion": string,              // 主要情绪，中文单词，例如：开心 / 阴阳 / 委屈 / 愤怒 / 无奈 / 鼓励 / 中性 / 困惑 / 害羞 / 傲娇
  "has_text": boolean,            // 图片上是否有可见的"字幕/对话框/标语/台词"（不含角落系列水印）
  "text_content": string | null   // 上述字幕逐字识别；多句之间用空格分隔；无字幕则 null
}

要求（极其重要）:
1. text_content 必须把图上的字幕/对话框/标语/台词一字不漏识别进去（即使文字小或不清晰）。
2. **忽略系列水印**：角落里固定出现的小水印（如 "Na"/"月巴"/"ANG" 这类非语义的系列标识、Logo、账号 ID 等）**不要写进 text_content / tags**，has_text 也以"是否有字幕"判断而非"是否有水印"。
3. 如果有字幕，把字幕按句拆开后也加进 tags（如"我等你回来" → 含"我等你回来"、"等你回来"、"等你"、"等待"）。
4. tags 10-20 个，**必须包含至少 6 个意图/情绪/语义类抽象词**（如 嫌弃 / 傲娇 / 阴阳怪气 / 敷衍 / 别理我 / 生无可恋 / 算了 / 我懒得理你），不要只描述外观和动作。其余可覆盖：场景、人物特征、动作、使用场景、角色名（仅在你 100% 确定时才标角色名）。每个 tag 是单独的中文短语，不带标点。
5. emotion 必须是中文词，不要用英文 joy/sad/anger。
6. 输出必须是合法 JSON，仅 JSON，无其他字符。`;

/**
 * Claude CLI provider 用（单张，XML 输出）。
 *
 * @param gridPath 已抽帧拼好的 4×4 拼图（GIF）或 PNG（静态图）路径
 * @param isGifGrid true 表示是 GIF 抽帧拼图，false 表示静态图
 */
export function buildUserPrompt(gridPath: string, isGifGrid: boolean): string {
  const intro = isGifGrid
    ? `请用 Read 工具读取图片：${gridPath}

注意：这是一张 GIF 动图按时间顺序抽 16 帧拼成的 4×4 宫格（左上→右下是时间序列）。请把它当作"同一个表情包的不同时刻"来理解，**不是 16 个不同的表情包**。字幕可能只在中间某几格出现，请扫完整段时间线再下结论。`
    : `请用 Read 工具读取图片：${gridPath}`;

  return `${intro}

严格按以下 XML 格式输出 5 个字段（每个标签必须出现），不要输出标签之外的任何文字：

<description>一句话描述图片（人物/动作/场景演变），不超过 30 字</description>
<emotion>中文情绪词：开心 / 阴阳 / 委屈 / 愤怒 / 无奈 / 鼓励 / 中性 / 困惑 / 害羞 / 难过 / 傲娇 等</emotion>
<has_text>true 或 false（小写，判断依据是"有无字幕"，不是"有无水印"）</has_text>
<text_content>字幕/对话框/标语/台词逐字识别，多句用空格分隔；无字幕时写 NONE。**不要把角落系列水印当字幕**（"Na" "月巴" "ANG" 这类非语义小水印一律忽略）</text_content>
<tags>
- tag1
- tag2
（共 10-20 个，每行一个，前面带 "- "）
</tags>

要求（极其重要）:
1. text_content 把图上字幕/对话框/标语/台词的每个汉字识别进去，一字不漏。${isGifGrid ? "宫格里 16 个时刻出现过的所有字幕都要识别。" : ""} **但角落里固定出现的小水印（系列标识 / Logo / 账号 ID 这类非语义字符如 "Na" "月巴" "ANG"）一律不算字幕，不写入 text_content。**
2. has_text 的判断：有字幕→true，只有水印→false。
3. 如果有字幕，按句拆开后也加进 tags（如"我等你回来" → 含"我等你回来"、"等你回来"、"等你"、"等待"）。
4. **tags 必须包含至少 6 个意图/情绪/语义类抽象词**（如 嫌弃 / 傲娇 / 阴阳怪气 / 敷衍 / 别理我 / 生无可恋 / 算了 / 我懒得理你），不要只描述外观和动作。其余可覆盖：场景、人物特征、动作、使用场景。tags 10-20 个，每个中文短语，不带标点。角色名仅在 100% 确定时标。**水印词（如 "Na" "月巴" "ANG"）一律不入 tags。**
5. emotion 用中文词，不要用英文。
6. ${isGifGrid ? "description 是描述整个表情包（整段动作/场景的演变），不要写成 16 张图各自描述。" : ""}`;
}

/**
 * Claude CLI 批量打标用（一个 session 跑多张图，节省 cache_creation 成本）。
 *
 * @param gridPaths N 张已抽帧拼好的 4×4 拼图路径
 */
export function buildBatchPrompt(gridPaths: string[]): string {
  const n = gridPaths.length;
  const list = gridPaths.map((p, i) => `${i + 1}. ${p}`).join("\n");
  return `请用 Read 工具依次读取下列 ${n} 张图片，对每张图输出一个 XML 块。

**关键**：每张图都是一个 GIF 动图按时间顺序抽 16 帧拼成的 4×4 宫格（左上→右下是时间序列）。要把每张拼图当作"同一个表情包的不同时刻"理解，不是 16 个不同表情包。字幕可能只在中间某几格出现，要扫完整段时间线再下结论。

图片清单（请严格按编号顺序处理）:
${list}

输出 ${n} 个 <image> 块，每张图一个块，格式严格如下：

<image>
<idx>1</idx>
<description>一句话描述这个表情包（整段动作/场景演变，不是描述 16 张图），≤30字</description>
<emotion>中文情绪词（开心 / 阴阳 / 委屈 / 愤怒 / 无奈 / 鼓励 / 中性 / 困惑 / 害羞 / 难过 / 傲娇 等）</emotion>
<has_text>true 或 false（依据"有无字幕"判断，不是"有无水印"）</has_text>
<text_content>字幕逐字识别（宫格 16 格出现过的所有字幕），多句用空格分隔；无字幕写 NONE。**忽略角落系列水印如 "Na" "月巴" "ANG"**</text_content>
<tags>
- tag1
- tag2
（10-20 个，每行一个，前面 "- "）
</tags>
</image>

要求（极其重要）:
1. 必须输出 ${n} 个 <image> 块，每张图一个，按编号 1 到 ${n} 顺序，不能漏。
2. <idx> 必须填对应编号（1 到 ${n}）。
3. text_content 把图上字幕/对话框/标语/台词的每个汉字识别进去，一字不漏。宫格里 16 个时刻出现过的所有字幕都要识别。**角落里固定出现的小水印（系列标识 / Logo 这类非语义字符如 "Na" "月巴" "ANG"）一律不算字幕，不写入 text_content，has_text 也不因此变 true，tags 里也不出现。**
4. 如果有字幕，按句拆开后也加进 tags（"我等你回来" → "等你回来"、"等你"、"等待"）。
5. **tags 必须包含至少 6 个意图/情绪/语义类抽象词**（嫌弃 / 傲娇 / 阴阳怪气 / 敷衍 / 别理我 / 生无可恋 等），不要只描述外观和动作。其余覆盖场景、人物、使用场景。tags 10-20 个，中文短语，不带标点。角色名仅 100% 确定时标。
6. emotion 用中文词，不要英文。
7. 仅输出 XML 块。不要任何前言、后语、解释、markdown 标记。
8. description 写整段表情包的描述，不要写成"16 张图各自描述"。即使到第 ${n} 张也要认真。`;
}

